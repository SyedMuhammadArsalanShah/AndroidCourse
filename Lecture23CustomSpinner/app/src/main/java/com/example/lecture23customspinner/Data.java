package com.example.lecture23customspinner;

class Data {

    String name;
    int image;

    public Data(String name, int image) {
        this.name = name;
        this.image = image;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImagee() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
